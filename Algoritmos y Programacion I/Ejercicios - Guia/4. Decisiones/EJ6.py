def dia_de_la_semana(dia): # de 1 a 366
	if dia % 7 == 0:
		return 'Domingo'
	if dia % 7 == 1:
		return 'Lunes'
	if dia % 7 == 2:
		return 'Martes'
	if dia % 7 == 3:
		return 'Miercoles'
	if dia % 7 == 4:
		return 'Jueves'
	if dia % 7 == 5:
		return 'Viernes'
	if dia % 7 == 6:
		return 'Sabado'

